package InnerClass;

public class tipObject {
	String tip;
	String link;
	public tipObject(String tip, String link) {
		super();
		this.tip = tip;
		this.link = link;
	}
	public String getTip() {
		return tip;
	}
	public void setTip(String tip) {
		this.tip = tip;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}

	
}
